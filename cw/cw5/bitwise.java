package cw5;

public class bitwise {
    public static void main(String[] args) {
        int c = 10;
        int a = 11;
        System.out.println(c | a);// 11
        System.out.println(c & a);// 10
        System.out.println(c >> 2);// right shift 2
        System.out.println(c << 2);// left shift 40
        System.out.println(c^a);//xor 5
        //xor=same zero different 1
    }
}
