package cw2_3_4basicsOfJava;

public class operator {
    public static void main(String[] args) {
        int a=5;
        // a++;
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(--a);
        System.out.println(a--);
        System.out.println(a);


    }
}
